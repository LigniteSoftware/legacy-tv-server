}

sub login {
  if (!defined($userEmail) || $userEmail eq '' || !defined($password) || $password eq '') {
    die "Unable to login: Unspecified username or password.\n" 
  }
  &pstatus("Logging in as \"$userEmail\"\n");
  if (!defined($ua)) {
    $ua = new LWP::UserAgent; 
    $ua->cookie_jar(HTTP::Cookies->new);
    $ua->proxy('http', $proxy) if defined($proxy);
    $ua->agent('Mozilla/4.0');
    $ua->default_headers->push_header('Accept-Encoding' => 'gzip, deflate');
  }

  my $rc = 0;
  while ($rc++ < $retries) {
    my $r = $ua->post($urlRoot . 'ZCLogin.do', 
      { 
        username => $userEmail, 
        password => $password,
        xhr => 'true',  # ? 
      }
    ); 
  
    $dc = Encode::encode('utf8', $r->decoded_content);
    if ($dc =~ /success,$userEmail/) {
      $ua->cookie_jar->set_cookie(0, 'gpv_pp', 'zap2it%20-%20tvlistings%20-%20grid%20-%20custom%20-%20descON%20-%20genreOFF%20-%20sixhour', '/', '.zap2it.com');
      $ua->cookie_jar->set_cookie(0, 'zap_isDescriptionOn', 'true', '/', '.zap2it.com');
      $ua->cookie_jar->set_cookie(0, 'isDescriptionOn', 'true', '/', '.zap2it.com');
      $ua->cookie_jar->set_cookie(0, 'userEmail', $userEmail, '/', '.zap2it.com');
      $ua->cookie_jar->set_cookie(0, 'postalcode', $postalcode, '/', '.zap2it.com') if (defined($postalcode));
      $ua->cookie_jar->set_cookie(0, 'lineupId', $lineupId, '/', '.zap2it.com') if (defined($lineupId));
      return $dc; 
    } else {
      &pstatus("[Attempt $rc] " . $dc);
    }
  }
  die "Failed to login within $retries retries.\n";
}

sub getURL {
  my $url = shift;
  if (!defined($ua)) {
    &login();
  }

  my $rc = 0;
  while ($rc++ < $retries) {
    &pstatus("Getting: $url\n");
    my $r = $ua->get($url);
    if ($r->is_success) {
      $tb += length($r->content);
      return $r->decoded_content;
    } else {
      &pstatus("[Attempt $rc] " . $r->status_line);
    }
  }
  die "Failed to download within $retries retries.\n";
}

sub headURL {
  my $url = shift;
  for (@fh) {return 0 if ($_ eq $url);}
  if (!defined($ua)) {
    &login();
  }
  &pstatus("Head: $url\n");
  my $r = $ua->head($url);
  push(@fh, $url) if (! $r->is_success);
  return $r->is_success;
}

sub wbf {
  my($f, $s) = @_;
  open(FO, ">$f");
  binmode(FO);
  print FO $s;
  close(FO);
}

sub copyLogo {
  my $key = shift;
  if (defined($iconDir) && defined($stations{$key}{logo})) {
    my $num = $stations{$key}{number};
    my $src = "$iconDir/" . $stations{$key}{logo} . $stations{$key}{logoExt};
    my $dest1 = "$iconDir/$num" . $stations{$key}{logoExt};
    my $dest2 = "$iconDir/$num " . $stations{$key}{name} . $stations{$key}{logoExt};
    copy($src, $dest1);
    copy($src, $dest2);
  }
}

sub handleLogo {
  my $url = shift;
  if (! -d $iconDir) {
    mkdir($iconDir) or die "Can't mkdir: $!\n";
  }
  ($n,$_,$s) = fileparse($url, qr"\..*");
  $stations{$cs}{logo} = $n;
  $stations{$cs}{logoExt} = $s;
  $f = $iconDir . "/" . $n . $s;
  if (! -e $f) { &wbf($f, &getURL($url)); }
}

sub setOriginalAirDate {
  if (substr($cp,10,4) ne '0000') {
    if (!defined($programs{$cp}{originalAirDate})
        || ($schedule{$cs}{$sch}{time} < $programs{$cp}{originalAirDate})) {
      $programs{$cp}{originalAirDate} = $schedule{$cs}{$sch}{time};
    }
  }
}

sub on_th {
  my($self, $tag, $attr) = @_;
  if (defined($attr->{class})) {
    if ($attr->{class} =~ /zc-st/) {
      $inStationTd = 1;
    }
  } 
}

sub on_td {
  my($self, $tag, $attr) = @_;
  if (defined($attr->{class})) {
    if ($attr->{class} =~ /zc-pg/) {
      if (defined($attr->{onclick})) {
        $cs = $rcs;
        $oc = $attr->{onclick};
        $oc =~ s/.*\((.*)\).*/$1/s;
        @a = split(/,/, $oc);
        $cp = $a[1];
        $cp =~ s/'//g;
        $sch = $a[2];
        if (length($cp) == 0) {
          $cp = $cs = $sch = -1;
          $expired++;
          $exp = 1;
        }
        $schedule{$cs}{$sch}{time} = $sch;
        $schedule{$cs}{$sch}{program} = $cp;
        $schedule{$cs}{$sch}{station} = $cs;

        if ($attr->{class} =~ /zc-g-C/) { $programs{$cp}{genre} = "Children" }
        elsif ($attr->{class} =~ /zc-g-N/) { $programs{$cp}{genre} = "News" }
        elsif ($attr->{class} =~ /zc-g-M/) { $programs{$cp}{genre} = "Movie" }
        elsif ($attr->{class} =~ /zc-g-S/) { $programs{$cp}{genre} = "Sports" }
      }
    } elsif ($attr->{class} =~ /zc-st/) {
      $inStationTd = 1;
    }
  } 
}

sub handleTags {
  my $text = shift;
  if ($text =~ /LIVE/) {
    $schedule{$cs}{$sch}{live} = 'Live';
    &setOriginalAirDate();
  } elsif ($text =~ /HD/) {
    $schedule{$cs}{$sch}{quality} = 'HD';
  } elsif ($text =~ /NEW/) {
    $schedule{$cs}{$sch}{new} = 'New';
    &setOriginalAirDate();
  }
}

sub on_li {
  my($self, $tag, $attr) = @_;
  if ($attr->{class} =~ /zc-ic-ne/) {
      $schedule{$cs}{$sch}{new} = 'New';
      &setOriginalAirDate();
  } elsif ($attr->{class} =~ /zc-ic/) { 
    $self->handler(text => sub { &handleTags(shift); }, "dtext");
  } elsif ($attr->{class} =~ /zc-icons-live/) {
      $schedule{$cs}{$sch}{live} = 'Live';
      &setOriginalAirDate();
  } elsif ($attr->{class} =~ /zc-icons-hd/) {
      $schedule{$cs}{$sch}{quality} = 'HD';
  }
}

sub on_img {
  my($self, $tag, $attr) = @_;
  if ($inIcons) {
    if ($attr->{alt} =~ /Live/) {
      $schedule{$cs}{$sch}{live} = 'Live';
      &setOriginalAirDate();
    } elsif ($attr->{alt} =~ /New/) {
      $schedule{$cs}{$sch}{new} = 'New';
      &setOriginalAirDate();
    } elsif ($attr->{alt} =~ /HD/ || $attr->{alt} =~ /High Definition/ 
      || $attr->{src} =~ /video-hd/ || $attr->{src} =~ /video-ahd/) {
      $schedule{$cs}{$sch}{quality} = 'HD';
    } 
  } elsif ($inStationTd && $attr->{alt} =~ /Logo/) {
    &handleLogo($attr->{src}) if defined($iconDir);
  }
}

sub on_a {
  my($self, $tag, $attr) = @_;
  if ($attr->{class} =~ /zc-pg-t/) {
    $self->handler(text => sub { $programs{$cp}{title} = (shift); $tba = 1 if $programs{$cp}{title} =~ /$sTBA/i;}, "dtext");
  } elsif ($inStationTd) {
    my $tcs = $attr->{href};
    $tcs =~ s/.*stnNum=(\w+).*/$1/;
    if (! ($tcs =~ /stnNum/)) {
      $cs = $rcs = $tcs;
    }
    if (!defined($stations{$cs}{stnNum})) {
      $stations{$cs}{stnNum} = $cs;
    }
    if (!defined($stations{$cs}{number})) {
      my $tnum = uri_unescape($attr->{href});
      $tnum =~ s/\s//gs;
      $tnum =~ s/.*channel=([.\w]+).*/$1/;
      $stations{$cs}{number} = $tnum if ! ($tnum =~ /channel=/);
    }
    if (!defined($postalcode) && $attr->{href} =~ /zipcode/) {
      $postalcode = $attr->{href};
      $postalcode =~ s/.*zipcode=(\w+).*/$1/;
    }
    if (!defined($lineupId) && $attr->{href} =~ /lineup/) {
      $lineupId = $attr->{href};
      $lineupId =~ s/.*lineupId=(.*?)&.*/uri_unescape($1)/e;
    }
  }
}

sub on_p {
